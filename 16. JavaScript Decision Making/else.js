const dayOfWeek = prompt("Enter a day of the Week").toLowerCase();

if (dayOfWeek === "monday") {
  console.log("I hate Mondays");
} else if (dayOfWeek === "saturday") {
  console.log("Yay I love Saturdays");
} else if (dayOfWeek === "friday") {
  console.log("TGIF");
} else {
  console.log("Meh");
}
